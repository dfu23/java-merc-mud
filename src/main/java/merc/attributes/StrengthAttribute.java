package merc.attributes;

public class StrengthAttribute {
	public short tohit;
	public short todam;
	public short carry;
	public short wield;
	
	public StrengthAttribute(short tohit, short todam, short carry, short wield) {
		this.tohit = tohit;
		this.todam = todam;
		this.carry = carry;
		this.wield = wield;
	}
	
}
